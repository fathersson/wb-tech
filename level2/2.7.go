package main

import (
	"fmt"
	"math/rand"
	"time"
)

func asChan(vs ...int) <-chan int {
	c := make(chan int)
	go func() {
		for _, v := range vs {
			c <- v
			time.Sleep(time.Duration(rand.Intn(1000)) * time.Millisecond)
		}
		close(c)
	}()
	return c
}

func merge(a, b <-chan int) <-chan int {
	c := make(chan int)
	go func() {
		for {
			select {
			case v, ok := <-a:
				if ok {
					c <- v
				} else {
					a = nil
				}
			case v, ok := <-b:
				if ok {
					c <- v
				} else {
					b = nil
				}
			}
			if a == nil && b == nil {
				close(c)
				return
			}
		}
	}()
	return c
}

func main() {
	rand.Seed(time.Now().Unix())
	a := asChan(1, 3, 5, 7)
	b := asChan(2, 4, 6, 8)
	c := merge(a, b)
	for v := range c {
		fmt.Print(v)
	}
}

// Программа выведет числа от 1 до 8, но их порядок будет случайным
// (за исключением того, что внутри своих групп 1,3,5,7 и 2,4,6,8
// последовательность сохранится)

// Объяснение работы select в этом конвейере:

// 1) Одновременное ожидание: Конструкция select внутри цикла for заставляет программу
// «слушать» оба канала (a и b) одновременно. Она не ждет сначала один, а потом другой,
// а берет данные из того, который готов в данный момент.

// 2) Обработка закрытия (Проверка ok): Когда данные в одном из каналов заканчиваются,
// функция asChan его закрывает. В select это ловится через флаг ok. Если ok == false,
// значит, канал пуст и закрыт.

// 3) Исключение канала через nil: Это самая важная деталь. В Go чтение из nil-канала
// блокируется навсегда. Присваивая a = nil или b = nil, мы «отключаем» этот case в select.
// Если этого не сделать, цикл превратится в бесконечный, постоянно считывая «пустые»
// значения из закрытого канала.

// 4) Завершение: Когда оба канала обнулены (a == nil && b == nil), функция merge понимает,
// что объединять больше нечего, закрывает выходной канал c и завершает горутину.

// Это классический пример паттерна Fan-in, который позволяет эффективно собирать данные
// из множества асинхронных источников в один поток.
